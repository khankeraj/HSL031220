package com.master.model;

public class ReadyModel {
	private int ready_id;
	private String ready_status;
	private String customer_name;
	public int getReady_id() {
		return ready_id;
	}
	public void setReady_id(int ready_id) {
		this.ready_id = ready_id;
	}
	public String getReady_status() {
		return ready_status;
	}
	public void setReady_status(String ready_status) {
		this.ready_status = ready_status;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}

}
