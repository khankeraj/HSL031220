package com.master.model;

public class RentalModel {
	
	private String rental_name;
	private String rental_description;
	private String rental_id;

	public String getRental_name() {
		return rental_name;
	}

	public void setRental_name(String rental_name) {
		this.rental_name = rental_name;
	}

	public String getRental_description() {
		return rental_description;
	}

	public void setRental_description(String rental_description) {
		this.rental_description = rental_description;
	}

	public String getRental_id() {
		return rental_id;
	}

	public void setRental_id(String rental_id) {
		this.rental_id = rental_id;
	}
}
